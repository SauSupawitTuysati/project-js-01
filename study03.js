// data type ชนิดข้อมูล
// คือ ตัวข้อมูล (literal) ที่เก็บไว้ในตัวแปร (letiable) หรือที่ทำไปใช้งาน
//------------------------------
// string
let string = 'asd';
console.log(string);

// number
let number = 123;
console.log(number);

// boolean
let boolean1 = true;
let boolean2 = false;
console.log(boolean1,boolean2);

// object
let object = {'dog':{color:'black'}};
console.log(object.dog.color);

// array
let array = ['ABC', 123, true];
console.log(array[0]);

// undefined
let undefined;
console.log(undefined);

// null
let nulls = null;
console.log(nulls);


// type checking การตรวจสอบชนิดข้อมูล
console.log(typeof(boolean2));